#include <iostream>

int main() {
    int w = 0;
    std::cin >> w;
    w += 1;
    while (!((w % 10 != (w % 100) / 10) &&
             (w % 10 != (w % 1000) / 100) &&
             (w % 10 != w / 1000) &&
             ((w % 100) / 10 != (w % 1000) / 100) &&
             ((w % 100) / 10 != w / 1000) &&
             ((w % 1000) / 100 != w / 1000))) {
        w += 1;
    }
    std::cout << w;
}
